    private void carregarEmpresas() {
        listaModelEmpresa = controllerEmpresa.getListaEmpresaAtivasController();

        DefaultTableModel modelo;
        modelo = (DefaultTableModel) jtbEmpresas.getModel();
        modelo.setNumRows(0);

        int cont = listaModelEmpresa.size();
        for (int i = 0; i < cont; i++) {

            modelo.addRow(new Object[]{
                listaModelEmpresa.get(i).getEmpCod(),
                listaModelEmpresa.get(i).getEmpRazao()
            });

        }
    }

