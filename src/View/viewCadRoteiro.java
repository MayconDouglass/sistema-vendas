    private void jtfIdTranspFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jtfIdTranspFocusLost
        String nomeTransp;
        int codigoEmpresa = Integer.parseInt(viewMainMenu.lblCodEmp.getText());
        if (!jtfIdTransp.getText().equals("")) {
            modelTransportadora = controllerTransportadora.getTransportadoraController(codigoEmpresa, Integer.parseInt(jtfIdTransp.getText()));
        } else {
            modelTransportadora.setTranspRaz(null);
        }

        if (jtfIdTransp.getText().equals("") || jtfIdTransp.getText() == null || modelTransportadora.getTranspRaz() == null) {
            JOptionPane.showMessageDialog(this, "O campo transportadora(ID) esta em branco!!", "Erro", JOptionPane.ERROR_MESSAGE);
            jtfNomeTransp.setText("Transportadora não encontrada!");
            jtfIdTransp.requestFocus();
            jtfIdTransp.setText("");
        } else {
            jtfNomeTransp.setText(modelTransportadora.getTranspRaz());
        }
    }//GEN-LAST:event_jtfIdTranspFocusLost

