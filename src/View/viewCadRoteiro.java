    private void salvarRoteiro() {
        int status = 0;
        modelRoteiro = controllerRoteiro.getConsultarRoteiroController(jtfDescRoteiro.getText(), Integer.parseInt(viewMainMenu.lblCodEmp.getText()));

        if (modelRoteiro.getCount() > 0) {
            JOptionPane.showMessageDialog(this, "Já existe um Roteiro com essa descrição nesta empresa!!", "Erro", JOptionPane.ERROR_MESSAGE);
        } else {
            modelRoteiro.setIdroteiro(Integer.parseInt(jtfID.getText()));
            modelRoteiro.setEmpcod(Integer.parseInt(viewMainMenu.lblCodEmp.getText()));
            modelRoteiro.setRotDesc(jtfDescRoteiro.getText());
            modelRoteiro.setTranspPadrao(Integer.parseInt(jtfIdTransp.getText()));
            modelRoteiro.setDatacad(bLDatas.retornarData());
            modelRoteiro.setDataalt("");
            modelRoteiro.setUsuariocad(viewMainMenu.lblUsuario.getText());
            modelRoteiro.setUsuarioalt("");

            String filtro = jcbStatus.getSelectedItem().toString();
            switch (filtro) {
                case "Ativo":
                    status = 1;
                    break;
                case "Inativo":
                    status = 0;
                    break;
            }
            modelRoteiro.setRotStatus(status);

            int proximo = Integer.parseInt(jtfID.getText());
            modelProximos.setIdkey(11);
            modelProximos.setProximo(proximo + 1);
            controllerProximos.atualizarSetProximosController(modelProximos);

            if (controllerRoteiro.salvarRoteiroController(modelRoteiro) > 0) {
                JOptionPane.showMessageDialog(this, "Erro ao salvar registro!!", "Erro", JOptionPane.ERROR_MESSAGE);

            } else {

                JOptionPane.showMessageDialog(this, "Roteiro salvo com sucesso!!", "Atenção", JOptionPane.WARNING_MESSAGE);
                viewMainRoteiro ViewMainRoteiro = new viewMainRoteiro();
                Desktop.add(ViewMainRoteiro);
                forms.centralizaForm(ViewMainRoteiro);
                ViewMainRoteiro.show();
                dispose();

            }

        }
    }

